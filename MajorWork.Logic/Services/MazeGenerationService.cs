        public int[] RandomDirections()
        {
            int[] randoms = new int[4];

            for (int i = 0; i < randoms.Length; i++)
            {
                randoms[i] = i;
            }

            Random rnd = new Random();

            for (int i = randoms.Length; i > 1; i--)
            {
                int a = rnd.Next(i);
                int tmp = randoms[a];
                randoms[a] = randoms[i - 1];
                randoms[i - 1] = tmp;
            }

            return randoms;
        }

    }
